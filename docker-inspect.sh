## Penggunaan Inspect digunakan untuk melihat detail dari aktivitas yang kita lakukan di dockert
## contohnya untuk mencek detail dari image, gunakan docker image inspect namaimage
## contohnya untuk mencek detail dari container gunakan docker container inspect namacontainer
## contohnya untuk mencek detail dari volume gunakan docker volume inspect namavolume
## contohnya untuk mencek detail dari network  gunakan docker network inspect namanetwork
